# Hello World를 7번 출력
def solution1():
    for i in range (7):
        print('Hello World')
    

#값을 할당한 변수를 7번 출력
def solution2():
    a = 1
    for i in range (7):
        print(a)

    
#1부터 10까지 더하기
def solution3():
    a = 0
    for i in range (11):
        a = a + i
    print(a)
    
